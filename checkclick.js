function myFINA320() {
    // Get the checkbox
    var checkBox = document.getElementById("myFINA320");
    // Get the output text
    var text = document.getElementById("320");

    // If the checkbox is checked, display the output text
    if (checkBox.checked == true){
    text.style.color = "green";
    }
    else {
    text.style.color = "red";
    }
}
